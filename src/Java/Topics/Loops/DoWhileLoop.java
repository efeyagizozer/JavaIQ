package Java.Topics.Loops;

public class DoWhileLoop {

    public static void main(String[] args) {
        int i = 0;

        // do-while döngüsü
        do {
            System.out.println("Current number: " + i);
            i++;
        } while (i < 5);

        System.out.println("Do-while loop is finished.");
    }
    }

